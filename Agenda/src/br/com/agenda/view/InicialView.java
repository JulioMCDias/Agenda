//-------marca posição na tabela caderno e habilita as abas ------
    private void configSelecaoT(int p){
        cTabelaCaderno.iniTCaderno();
        if(jTableAgendas.getRowCount()>0){
            jTableAgendas.setRowSelectionInterval(0, p);
            cAbaContato.bloquearLiberarCampos(true);
            jLabelCaderno.setText(cTabelaCaderno.getAgendas().get(p).getNome());
            cTabelaContato.carregarTContato(cTabelaCaderno.getAgendas().get(p));
        }
    }
    
    
    private void salvarContato(){
        if(!jTextFieldNome.getText().equals("")){
            if(jToggleButtonEditarContato.isSelected()){
                int row = jTableResultadoPesquisa.getSelectedRow();
                Contato c = cTabelaContato.getContatos().get(row);
                getCampos(c);
                cTabelaContato.atualizarContatos(c);
            } else{
                Contato c = new Contato();
                getCampos(c);
                cTabelaContato.inserirContatos(c); 
            }
        }else
            JOptionPane.showMessageDialog(null, "Contato sem nome");
        jToggleButtonEditarContato.setSelected(false);
    }

    private void limparCampos(){
        jTextFieldNome.setText("");
        jFormattedTextFieldCel.setText("");
        jTextFieldCidade.setText("");
        jFormattedTextFieldTel.setText("");
        jTextFieldEmail.setText("");
        jTextFieldEnd.setText("");
        jFormattedTextFieldDataNaci.setText("");
        jTextAreaObservacao.setText("");
        jToggleButtonEditarContato.setSelected(false);
        jToggleButtonExcluirContato.setSelected(false);
    }
    
    private void getCampos(Contato c){
        c.setNome(jTextFieldNome.getText());
        c.setTelefoneCelular(jFormattedTextFieldCel.getText());
        c.setCidade(jTextFieldCidade.getText());
        c.setTelefoneFixo(jFormattedTextFieldTel.getText());
        c.setEmail(jTextFieldEmail.getText());
        c.setEndereco(jTextFieldEnd.getText());
        c.setDataNacimento(jFormattedTextFieldDataNaci.getText());
        c.setObservacao(jTextAreaObservacao.getText()); 
        limparCampos();      
    }
    
    private void setCampos(int row){
        
        Contato c = cTabelaContato.getContatos().get(row);
        jTextFieldNome.setText(c.getNome());
        jFormattedTextFieldCel.setText(c.getTelefoneCelular());
        jTextFieldCidade.setText(c.getCidade());
        jFormattedTextFieldTel.setText(c.getTelefoneFixo());
        jTextFieldEmail.setText(c.getEmail());
        jTextFieldEnd.setText(c.getEndereco());
        jFormattedTextFieldDataNaci.setText(c.getStringDataNac());
        jTextAreaObservacao.setText(c.getObservacao());  
    }
    
    
    private void telaEscolherCaderno(){
        if(jTableAgendas.getSelectedRow() == -1){
        EscolherAgendaView selecionarAgenda = new EscolherAgendaView(cTabelaCaderno.getAgendas());
        selecionarAgenda.setVisible(true);
        int p = selecionarAgenda.getPosicao();
        if(p !=-1)
            configSelecaoT(p);
        selecionarAgenda.dispose(); 
        }
    }
    
