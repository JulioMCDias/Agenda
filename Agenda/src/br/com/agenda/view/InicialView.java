    private void jTableResultadoPesquisaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableResultadoPesquisaMouseClicked
        int row = jTableResultadoPesquisa.getSelectedRow();
        if(row !=-1){
//------controle da caixa de texto obsevação----
            List<Contato> c = cTabelaContato.getContatos();
            jTextAreaObservacao1.setText(c.get(row).getObservacao());

    //------controle de excluir contatos------
            if(jToggleButtonExcluirContato.isSelected()){
                cTabelaContato.excluirContatos(row);
                jToggleButtonExcluirContato.setSelected(false);
                jTextAreaObservacao1.setText("");
//------controle de editar contatos------            
            }else if(jToggleButtonEditarContato.isSelected()){
                jTabbedPane1.setSelectedIndex(1);       //aba de criar contato/editar
                setCampos(row);
            }
        }else
            jTextAreaObservacao1.setText("");        
    }//GEN-LAST:event_jTableResultadoPesquisaMouseClicked

