    private void jToggleButtonExcluirContatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButtonExcluirContatoActionPerformed
        if(jTableAgendas.getRowCount() ==0){
            JOptionPane.showMessageDialog(null, "Crie uma agenda primeiro");
            jToggleButtonExcluirContato.setSelected(false);
        }else {
            if(jToggleButtonExcluirContato.isSelected()){
                if(jToggleButtonEditarContato.isSelected())
                    jToggleButtonEditarContato.setSelected(false);
                telaEscolherCaderno();

                JOptionPane.showMessageDialog(null, "Selecione o contato para excluir");
                cTabelaContato.iniTContato();
                jTabbedPane1.setSelectedIndex(0);       //aba de pesquisa
            }
        }    
    }//GEN-LAST:event_jToggleButtonExcluirContatoActionPerformed

